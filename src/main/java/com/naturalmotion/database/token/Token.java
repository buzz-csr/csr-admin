package com.naturalmotion.database.token;

public class Token {

	private String crew;

	private Card bronze;

	private Card silver;

	private Card gold;

	public String getCrew() {
		return crew;
	}

	public void setCrew(String crew) {
		this.crew = crew;
	}

	public Card getBronze() {
		return bronze;
	}

	public void setBronze(Card bronze) {
		this.bronze = bronze;
	}

	public Card getSilver() {
		return silver;
	}

	public void setSilver(Card silver) {
		this.silver = silver;
	}

	public Card getGold() {
		return gold;
	}

	public void setGold(Card gold) {
		this.gold = gold;
	}

}
